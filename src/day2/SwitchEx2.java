package day2;

public class SwitchEx2 {

	public static void main(String[] args) {
	char ch = 'C';
		
		switch(ch){
		case 'a':
			System.out.println("a 입니다");
			break;
		case 'A':
			System.out.println("A 입니다");
			break;
		case 'b':
			System.out.println("b 입니다");
			break;
		case 'B':
			System.out.println("B 입니다");
			break;
		default:
			System.out.println("A,a,B,b 가 아닙니다");
		}
		
	}

}
